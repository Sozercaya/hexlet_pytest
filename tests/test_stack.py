def test_stack_main_scenario():
    """
    Тест основной функциональности (позитивный сценарий).
    Проверяем, что стек работает по принципу LIFO: последний пришел - первый ушел.
    """
    stack = []                 # Создаем пустой стек
    stack.append("one")        # Добавляем первый элемент
    stack.append("two")        # Добавляем второй элемент

    # Извлекаем элементы и проверяем, что они возвращаются в обратном порядке
    assert stack.pop() == "two"   # Последний добавленный должен выйти первым
    assert stack.pop() == "one"   # Первый добавленный выходит последним


def test_emptiness():
    """
    Тест дополнительной функциональности.
    Проверяем поведение стека с пустотой в разных состояниях.
    """
    stack = []
    # Проверка 1: Новый стек должен быть пустым
    assert not stack            # not [] возвращает True, так как пустой список - ложь
    
    stack.append("one")
    # Проверка 2: После добавления стек не должен быть пустым
    assert stack                # Непустой список - истина
    
    stack.pop()
    # Проверка 3: После извлечения всех элементов стек снова пуст
    assert not stack


